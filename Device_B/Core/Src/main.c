/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_SPI2_Init();
  MX_USART1_UART_Init();
  MX_CRC_Init();
  /* USER CODE BEGIN 2 */

  //nRF Config
  nRF24_Init(&hspi2);

  //Random message generation- First message is the same after each reset until additional source of rng will be addded
  RSA_Driver_Get_Random_Message();

  __CRC_CLK_ENABLE();
  nRF24_SetRXAddress(0,(uint8_t *)"Nad");
  nRF24_SetTXAddress((uint8_t *)"Nad");

  //IR Config
  ir_sender_init();
  ir_receiver_init();


  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

	  switch (transmission_step) {
	    case 0: {
	      value = ir_read();
	      if (value != -1) {
	        if (value >= 10 || value <= 255) {
	          ir_disable_interrupt();
	          HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
			    /////////////////////////////////////WIADOMOSC TESTOWA/////////////////////////////////////////
			    HAL_UART_Transmit(&huart1, "Numer pojazdu:\n", 15, 1000);
			    HAL_UART_Transmit(&huart1, value + "\n", 2, 1000);
			    /////////////////////////////////////WIADOMOSC TESTOWA/////////////////////////////////////////
	          transmission_step = 1;
	        }
	      }
	      break;
	    }

	    case 1: {
	      currentTime = HAL_GetTick();  // Pobranie aktualnego czasu w ms
	      if ((currentTime - lastCommandTime) >= 1000) {
	        NEC_SendCommand(command);       // Wysłanie komendy
	        lastCommandTime = currentTime;  // Aktualizacja czasu ostatniego wysłania
	        ir_send_counter += 1;
	      }
	      if (ir_send_counter == 6) {
	        transmission_step = 2;
	      }
	      break;
	    }

	    case 2: {
	    	Encrypt_And_Add_Random_Message(value-10);
	      nRF24_TX_Mode();
	      transmission_step = 3;
	      break;
	    }

	    case 3: {
	      sd_status = send_message(50);
	      if (sd_status == 1) {
	        transmission_step = 4;
	      }
	      break;
	    }

	    case 4: {
	      nRF24_RX_Mode();

	      transmission_step = 5;
	      break;
	    }

	    case 5: {
	      rv_status = receive_message();
	      if (rv_status == 1) {
	        transmission_step = 6;
	      }
	      break;
	    }
	    case 6: {
	      HAL_GPIO_TogglePin(LD3_GPIO_Port, LD3_Pin);
	      transmission_step = 7;
	      break;
	    }

	    case 7: {
	      transmission_step = 0;
	      //value = -1;
	      ir_enable_interrupt();
	      ir_send_counter = 0;
	      RSA_Driver_Get_Random_Message();
	      break;
	    }
	  }


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

